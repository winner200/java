public class User {

    // 属性【以下都是成员变量之实例变量】

    // 用户编号
    // int 是一种基本数据类型：整数型
    // no 是一个实例变量
    int no;

    // 用户名
    // String是一种引用数据类型：代表字符串
    // name 是一个实例变量
    // name 是一个引用
    String name;

    // 家庭住址
    // Address 是一种引用数据类型：代表家庭住址
    // addr是一个实例变量
    // addr 是一个引用
    Address addr;
}